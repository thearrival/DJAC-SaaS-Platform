export function localizeRelationship(locale: Locale, relationship: RelationshipLike): RelationshipLike & { actionRecommendation?: string | null } {
  const directKey = `${relationship.sourceDirection ?? ""}->${relationship.targetDirection ?? ""}:${relationship.relationshipType}`;
  const reverseKey = `${relationship.targetDirection ?? ""}->${relationship.sourceDirection ?? ""}:${relationship.relationshipType}`;
  const translation = relationshipText[directKey] ?? relationshipText[reverseKey];
  return {
    ...relationship,
    description: pick(locale, translation?.description, relationship.description ?? ""),
    mitigation: pick(locale, translation?.mitigation, relationship.mitigation ?? ""),
    actionRecommendation: localizeRelationshipAction(locale, relationship.relationshipType),
  };
}

